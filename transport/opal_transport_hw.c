typedef enum {
    HW_NEXT_DISCOVERY,
    HW_NEXT_SES_OPEN,
    HW_NEXT_SUCCESS,
    HW_NEXT_AUTH_FAIL,
} opal_hw_next_op_t;

typedef struct {
    opal_hw_next_op_t next_op;
    uint32_t send_count;
    uint32_t recv_count;
} opal_hw_ctx_t;

